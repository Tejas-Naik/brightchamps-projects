import math

# getting a square root
x = 625
root_of_5 = math.sqrt(x)
print(root_of_5)

# Getting a power
y = 25
power_of_2 = math.pow(y,2)
print(power_of_2)

# Rounding up a number
z = 3.1415962
print(math.ceil(z))

# returns a positive value
print(math.fabs(-45.23))

# returns the factorial of a number
print(math.factorial(5))



import math

# calculating the square root
x = 49
square_root = math.sqrt(x)
print(square_root)

# Calculating the Power of a number
base = 3
exponent = 4
result = math.pow(base, exponent)
print(result)

# Rounding Numbers Up and Down
number = 5.3
ceiling = math.ceil(number)
floor = math.floor(number)
print(ceiling, floor)

